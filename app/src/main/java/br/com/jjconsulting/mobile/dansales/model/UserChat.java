package br.com.jjconsulting.mobile.dansales.model;

public class UserChat {

    private String name;
    private String email;
    private String legado_id;


    public UserChat(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLegado_id() {
        return legado_id;
    }

    public void setLegado_id(String legado_id) {
        this.legado_id = legado_id;
    }
}


